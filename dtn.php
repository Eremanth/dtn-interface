      <p class="style40" align="justify">
La direction technique nationale est responsable <b>du rep&eacute;rage 
et du suivi des meilleurs potentiels et internationaux fran&ccedil;ais</b>. 
Son action est fastidieuse, r&eacute;p&eacute;titive, voire administrative et laborieuse, mais avant tout fondamentale. 
Elle tire son essence de la n&eacute;cessit&eacute; &eacute;vidente d'un soutien logistique basique 
aux s&eacute;lectionneurs, et de la logique conjoncturelle qui a suivi la croissance de la France-HT.
<p class="style40" align="justify">
M&ecirc;me si nous avons v&eacute;cu des moments difficiles par le pass&eacute;, 
les organisations hi&eacute;rarchique et op&eacute;rationnelle sont bien r&ocirc;d&eacute;es aujourd'hui. 
Ainsi, <b>l'effectif est en &eacute;volution permanente et continue</b>; 
vous pouvez le consulter sur la page des <u><a href="dtn_members.php">membres</a></u>.
</p><p class="style40" align="justify">
Chaque membre, aussi appel&eacute; DTN, n'est en charge que d'un certain nombre de joueurs d'une m&ecirc;me position 
(commun&eacute;ment celle entra&icirc;n&eacute;e). Ils doivent effectuer 
<b>des mises-&agrave;-jour r&eacute;guli&egrave;res, en lien direct avec les propri&eacute;taires des joueurs</b>. 
Lors de ces &eacute;changes, les donn&eacute;es &agrave; recueillir vont de toutes les caract&eacute;ristiques du joueur, 
dont notamment le nombre de semaines d'entra&icirc;nement depuis la derni&egrave;re progression, 
&agrave; la formation planifi&eacute;e par le manager, en passant par la pr&eacute;vision d'arr&ecirc;t de 
l'entra&icirc;nement ou de vente du joueur concern&eacute;.
</p><p class="style40" align="justify">
Comme cela se comprend rapidement, le r&ocirc;le central d'un membre de la direction technique national, 
au-del&agrave; du simple surveillant, est celui d'un v&eacute;ritable communicant. 
<b>Les relations avec les propri&eacute;taires des meilleurs potentiels et internationaux fran&ccedil;ais 
sont absolument essentielles!</b>
</p><p class="style40" align="justify"><br>
La DTN fonctionne de mani&egrave;re priv&eacute;e, mais elle publie une partie importante de ses donn&eacute;es, 
dans un souci de communication envers chacun, <b>pour plus de transparence et d'&eacute;mulation nationale</b>. 
Ainsi, un nombre int&eacute;ressant de classements, bas&eacute;s sur une r&eacute;f&eacute;rence internationale, 
est disponible sur la page des <u><a href="dtn_tops.php">tops</a></u>.
Nous publions &eacute;galement les diff&eacute;rents pr&eacute;-requis et profils recherch&eacute;s qui concernent 
les s&eacute;lections fran&ccedil;aises, aussi bien <u><a href="dtn_u20.php">u20</a></u> que <u><a href="dtn_a.php">A</a></u>.
</p><p class="style40" align="justify">
Pour finir, et pour nous aider dans notre travail de rep&eacute;rage, nous vous invitions &agrave; soumettre 
votre &eacute;quipe &agrave; l'analyse des pr&eacute;-requis et profils pr&eacute;cit&eacute;s, 
sur la page <u><a href="dtn_submitting.php">suivante</a></u>.
</p>      
      
