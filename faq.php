	        <b><a name="2" id="2"></a><span class="style172">2| Le suivi des joueurs.</span></b><br />            
	        <b><a name="2a" id="2a"></a>Comment  ajouter son joueur &agrave; la base de donn&eacute;es de la DTN ?</b><br />
	        C'est extr&ecirc;mement simple, il vous suffit d'aller sur <a href="https://www.ht-dtnfff.fr/"><strong>la page du site de la DTN</strong></a>  et  taper votre alias-HT (votre nom d&rsquo;utilisateur sur Hattrick) ainsi que votre
	        code de s&eacute;curit&eacute; dans les champs pr&eacute;vus &agrave; cet effet ; le code de  s&eacute;curit&eacute; n&rsquo;est pas le mot de passe, et ne
	        donne qu&rsquo;un acc&egrave;s en lecture, il est &agrave; d&eacute;finir dans &lsquo;mon  hattrick&rsquo;, onglet &lsquo;pr&eacute;f&eacute;rences&rsquo;, menu &lsquo;code de
	        s&eacute;curit&eacute;&rsquo;.<br />
	        La proc&eacute;dure cherchera parmi tous vos joueurs fran&ccedil;ais ceux qui  correspondent aux minima et les ins&egrave;rera
	        directement dans la base de donn&eacute;es. Tout comme le site  ht-fff.org, la proc&eacute;dure est CHPP (Certified Hattrick
	        Product Provider) ; cela signifie que ht-fff.org utilise des  informations provenant du service du jeu en ligne
	        hattrick.org, et que cette utilisation a &eacute;t&eacute; approuv&eacute;e par les  d&eacute;veloppeurs et d&eacute;tenteurs des droits de
	        hattrick.org, Extralives AB.<br />
	        <br />	                  
	        <b><a name="2b" id="2b"></a>Qu'est-ce  qu'un joueur &quot;rep&eacute;r&eacute;&quot; ?</b><br />
	        Un joueur &quot;rep&eacute;r&eacute;&quot; est un joueur dont le potentiel peut  int&eacute;resser une des s&eacute;lections nationales fran&ccedil;aises, et qui
	        est d&eacute;tect&eacute; par la DTN.<br />
	        En g&eacute;n&eacute;ral, il s'agit d'un joueur en vente, ou mis en &eacute;vidence sur  le sujet du forum France [[Senior/YP] Jeunes promus]. 
	        Mais cela peut &eacute;galement &ecirc;tre un joueur d&eacute;tect&eacute;  via des sites tel &lsquo;Alltid Hattrick&rsquo;
	        (http://alltid.org/) pour ne citer que celui ci.<br />
	        Le propri&eacute;taire est alors contact&eacute; par un membre de la DTN, qui  lui demandera d'inscrire le joueur dans la base
	        de donn&eacute;es (pour fiabiliser et faciliter sa visibilit&eacute; pour le  s&eacute;lectionneur et son staff), et des questions lui seront
	        pos&eacute;es sur son entra&icirc;nement &agrave; court et moyen terme.<br />
	        <br />	                  
	        <b><a name="2c" id="2c"></a>Qu'est-ce  qu'un joueur &quot;suivi&quot; ?</b><br />
	        Un joueur &quot;suivi&quot; est un joueur pr&eacute;sent dans la base de  donn&eacute;es de la DTN, et qui fait partie des meilleurs
	        potentiels de sa classe d&rsquo;&acirc;ge. Il peut s'agir d'un joueur ayant  une excellente caract&eacute;ristique primaire, ou d'un
	        joueur plus polyvalent.<br />
	        Ce joueur se voit plac&eacute; sous la surveillance d'un DTN, qui va  rester en contact avec le propri&eacute;taire, veiller &agrave; la
	        bonne marche de l'entra&icirc;nement semaine apr&egrave;s semaine afin  d'affiner au maximum la pr&eacute;cision de la base de
	        donn&eacute;es. Cet &eacute;l&eacute;ment est important, car la diff&eacute;rence entre les  joueurs qui int&egrave;grent les &eacute;quipes de France et
	        ceux pour lesquels la porte reste malheureusement ferm&eacute;e est  souvent de l'ordre de la semaine
	        d'entra&icirc;nement, m&ecirc;me si d&rsquo;autres &eacute;l&eacute;ments sont &eacute;galement pris en  compte.<br />
	        Un DTN suit en g&eacute;n&eacute;ral une douzaine de joueurs, et joue le r&ocirc;le  d&rsquo;interface entre s&eacute;lectionneurs et propri&eacute;taires,
	        par l&rsquo;interm&eacute;diaire de la DTN, sachant que les s&eacute;lectionneurs  contactent directement les propri&eacute;taires en cas
	        de doutes ou de besoins.<br />
	        <br />	                  
	        <b><a name="2d" id="2d"></a>Pourquoi  un joueur cesse-t-il d'&ecirc;tre int&eacute;ressant pour la DTN?</b> <br />
	        <b>Que  se passe-t-il alors ?</b><br />
	        Lorsqu'un joueur est trouv&eacute; trop en retard par rapport aux  meilleurs repr&eacute;sentants de sa classe d&rsquo;&acirc;ge, quel
	        qu&rsquo;en soit la cause, son DTN le propose g&eacute;n&eacute;ralement &agrave; la &laquo; purge  &raquo;. A chaque fin de mois, le sujet de purge
	        est analys&eacute; et trait&eacute;, et si jamais le joueur n&rsquo;a plus aucun  int&eacute;r&ecirc;t pour les s&eacute;lections il est purg&eacute; ; cela signifie qu'il
	        est archiv&eacute; dans la base de donn&eacute;es, n&rsquo;est plus mis &agrave; jour, et par  cons&eacute;quent n'appara&icirc;t plus dans les
	        classements. Ceci a pour  but d'all&eacute;ger la base de donn&eacute;es, et de mettre
	        un terme &agrave; un travail d&eacute;sormais inutile.<br />
	        Un joueur peut perdre de son int&eacute;r&ecirc;t pour diverses raisons :  disparition pure et simple (licenciement, botification
	        du propri&eacute;taire), fin de l'entra&icirc;nement (pour cause de vente  souvent en lien avec des consid&eacute;rations salariales,
	        ou suite &agrave; un changement d&rsquo;entra&icirc;nement aussi appel&eacute; &laquo; switch &raquo;),  blessures longues ou r&eacute;p&eacute;t&eacute;es&hellip;<br />
	        <br />	                  
	        <b><a name="2e" id="2e"></a>Combien  de joueurs se trouvent dans la base de donn&eacute;es ?</b><br />            
	        <b>Quelle  est leur r&eacute;partition par &acirc;ge ?</b><br />
	        La base de donn&eacute;es contient approximativement 2200 joueurs (sans  compter les archives).<br />
	        La r&eacute;partition par &acirc;ge est difficile &agrave; donner pr&eacute;cis&eacute;ment, mais en  voici une estimation :<br />
	        &#8226; 17 ans: 35%<br />
	        &#8226; 18 ans: 25%<br />
	        &#8226; 19 ans: 10%<br />
	        &#8226; 20 ans: 10%<br />
	        &#8226; 21 ans et plus: 10%<br />
	        <br />	        
	        <b><a name="2f" id="2f"></a>Comment  travaillent les s&eacute;lectionneurs et leurs staffs avec la DTN ?</b><br />
	        Juste apr&egrave;s chaque r&eacute;sultat d&rsquo;&eacute;lection, l&rsquo;administrateur (DTN#)  contacte l&rsquo;heureux &eacute;lu pour lui indiquer un des
	        d&eacute;veloppeurs (DTN~) afin que ce dernier lui transmette le plus t&ocirc;t  possible les codes d&rsquo;acc&egrave;s &lsquo;s&eacute;lectionneurs&rsquo; &agrave;
	        la base de donn&eacute;es de la DTN (l&rsquo;administrateur ne conna&icirc;t pas les  codes d&rsquo;acc&egrave;s &lsquo;s&eacute;lectionneurs&rsquo;).<br />
	        Si le s&eacute;lectionneur le souhaite (ce qui est tr&egrave;s g&eacute;n&eacute;ralement le  cas), il utilisera alors l&rsquo;ensemble des moyens de
	        la Direction Technique Nationale. Ainsi, il peut consulter le  travail r&eacute;alis&eacute; tout au long de la saison par la DTN,
	        b&eacute;n&eacute;ficier d&rsquo;une vision compl&egrave;te sur les meilleurs potentiels du  vivier fran&ccedil;ais en un seul coup d&rsquo;oeil, et influer
	        sur les orientations futures en exprimant ses besoins &agrave; court et  moyen terme.<br />
	        <br />
	        La DTN est l&agrave; pour aider les s&eacute;lectionneurs et leurs staffs &agrave;  faire leurs choix dans les meilleures dispositions
	        possibles, afin de prendre les d&eacute;cisions qui sont le plus en phase  avec leurs projets pour la s&eacute;lection nationale.<br />
