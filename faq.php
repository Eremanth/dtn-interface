	          <b><a name="3a" id="3a"></a>Je  souhaite entra&icirc;ner un espoir fran&ccedil;ais, comment faire ?</b><br />
	          Il convient tout d'abord d'avoir un entra&icirc;neur de bon niveau  (honorable, voire excellent) et un peu d'argent de
	        cot&eacute; ; en cas de blessure du joueur il peut &ecirc;tre n&eacute;cessaire de  prendre de nombreux (et donc co&ucirc;teux)
	        m&eacute;decins.<br />
	        <br />
	        Pour les joueurs de l'&eacute;quipe U20 (celle des joueurs &acirc;g&eacute;s de 20 ans  et moins), le salaire n'est pas un &eacute;l&eacute;ment
	        significatif. Par contre, le salaire est &agrave; int&eacute;grer dans le plan  de financement pour les joueurs visant l'&eacute;quipe de
	        France A, tendant &agrave; devenir la donn&eacute;e principale dans la plupart  des cas.<br />
	        <br />
	        Il faut juste &ecirc;tre s&eacute;rieux et le faire jouer chaque semaine afin  qu'il re&ccedil;oive son entra&icirc;nement. Le faire jouer en
	        coupe si on la dispute toujours. Le soigner quand il est bless&eacute;.<br />
	        Mettre son joueur &agrave; jour dans la base de donn&eacute;es de la DTN &agrave;  chaque prise (hausse) de niveau afin qu'il soit
	        suivi et que le s&eacute;lectionneur puisse avoir un oeil dessus.<br />
	        Il faut &ecirc;tre patient et chanceux, mais aussi &ecirc;tre rigoureux.<br />
	        <br />	        
	        <b><a name="3b" id="3b"></a>J'ai  sorti un jeune de mon centre, peut-il un jour &ecirc;tre en U20 ?</b><br />
